    public static TreeNode lowestCommonAncestor(TreeNode root, TreeNode p, TreeNode q) {
        while (true) {
            if (root.val > p.val && root.val > q.val) {
                root = root.left;
            }else if (root.val < p.val && root.val < q.val) {
                root = root.right;
            }else { //ä¸€å®šéœ€è¦elseï¼Œå¦åˆ™éƒ½ä¼šæ‰§è¡Œbreakäº†
                break;
            }
        }

        return root;
    }

