class MinStack {
    //å€ŸåŠ©ä¸¤ä¸ªstack
    LinkedList<Integer> originalStack;
    LinkedList<Integer> minStack;

    public MinStack() {
        originalStack = new LinkedList();
        minStack = new LinkedList<>();
        //è¿™ä¸ªæ¯”è¾ƒå…³é”®ï¼Œéœ€è¦é¢„è®¾ä¸€ä¸ªå€¼ï¼Œç„¶åæ‰èƒ½æ¯”è¾ƒ
        minStack.push(Integer.MAX_VALUE);
    }
    
    public void push(int val) {
        originalStack.push(val);
        minStack.push(Math.min(minStack.peek(),val));
    }
    
    public void pop() {
        originalStack.pop();
        minStack.pop();
    }
    
    public int top() {
        return originalStack.peek();
    }
    
    public int getMin() {
        return minStack.peek();
    }
}

