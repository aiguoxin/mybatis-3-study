import java.util.LinkedList;
import java.util.List;
import leetcode.bfs.TreeNode;

/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution032117 {
    public static List<Integer> inorderTraversal(TreeNode root) {
        List<Integer> list = new LinkedList<>();
        if(root == null){
            return list;
        }
        middle(root, list);
        return list;
    }

    private static void middle(TreeNode root, List<Integer> list) {
        if (root.left != null) {
            middle(root.left, list);
        }

        list.add(root.val);

        if (root.right != null) {
            middle(root.right, list);
        }
    }

    public static void main(String[] args) {
        //root = [1,null,2,3]
        //è¾“å‡ºï¼š[1,3,2]
        TreeNode n1 = null;
//        TreeNode n2 = new TreeNode(2);
//        TreeNode n3 = new TreeNode(3);
//
//        n1.right = n2;
//        n2.left = n3;
        List<Integer> list = inorderTraversal(n1);
        list.stream().forEach(num -> System.out.println(num));
    }
}
